BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS daily_health_scores (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id INTEGER NOT NULL,
    score_date DATE NOT NULL,
    calories_percentage DECIMAL(5,2),
    protein_percentage DECIMAL(5,2),
    carbs_percentage DECIMAL(5,2),
    fats_percentage DECIMAL(5,2),
    calories_score DECIMAL(5,2),
    protein_score DECIMAL(5,2),
    carbs_score DECIMAL(5,2),
    fats_score DECIMAL(5,2),
    final_score DECIMAL(4,2),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    UNIQUE (user_id, score_date)
);
CREATE TABLE IF NOT EXISTS daily_user_stats (
    id INTEGER PRIMARY KEY,
    user_id INTEGER NOT NULL,
    date DATE NOT NULL,
    weight FLOAT,
    age TEXT,
    activity BLOB CHECK(activity IN ('No Exercise', 'Once a week', '2-3 time per week', '4-5 times a week')),
    goal BLOB CHECK(goal IN ('maintain weight', 'lose weight', 'gain weight')),
    required_calories FLOAT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id),
    UNIQUE(user_id, date)
);
CREATE TABLE IF NOT EXISTS foods (
	id INTEGER NOT NULL, 
	name VARCHAR(255), 
	calories FLOAT, 
	PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS "intake" (
    "id" INTEGER NOT NULL,
    "user_id" INTEGER NOT NULL,
    "foods_id" INTEGER,  -- Made this nullable
    "food_name" TEXT,
    "count" FLOAT,
    "date" DATETIME,
    "food_calories" FLOAT,
    "food_carbs" FLOAT DEFAULT 0,
    "food_protein" FLOAT DEFAULT 0,
    "food_fats" FLOAT DEFAULT 0,
    PRIMARY KEY("id" AUTOINCREMENT),
    FOREIGN KEY("user_id") REFERENCES "users"("id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS recipe_ingredients (
    id SERIAL PRIMARY KEY,
    recipe_id INTEGER REFERENCES user_recipes(id) ON DELETE CASCADE,
    ingredient_name VARCHAR(255) NOT NULL,
    calories_per_gram FLOAT NOT NULL,
    carbs_per_gram FLOAT NOT NULL,
    protein_per_gram FLOAT NOT NULL,
    fats_per_gram FLOAT NOT NULL,
    amount FLOAT NOT NULL
);
CREATE TABLE IF NOT EXISTS "user_foods" (
    "id" INTEGER,
    "user_id" INTEGER NOT NULL,
    "name" VARCHAR(255) NOT NULL,
    "calories" FLOAT NOT NULL,
    "carbs" FLOAT DEFAULT 0,
    "protein" FLOAT DEFAULT 0,
    "fats" FLOAT DEFAULT 0,
    "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY("id" AUTOINCREMENT),
    UNIQUE("user_id","name"),
    FOREIGN KEY("user_id") REFERENCES "users"("id")
);
CREATE TABLE IF NOT EXISTS user_recipes (
       id INTEGER PRIMARY KEY AUTOINCREMENT,
       user_id INTEGER NOT NULL,
       recipe_name VARCHAR(255) NOT NULL,
       calories_per_gram FLOAT NOT NULL,
       carbs_per_gram FLOAT NOT NULL,
       protein_per_gram FLOAT NOT NULL,
       fats_per_gram FLOAT NOT NULL,
       created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
   );
CREATE TABLE IF NOT EXISTS "users" (
	"id"	INTEGER NOT NULL,
	"username"	VARCHAR(255),
	"password"	VARCHAR(255),
	"role"	VARCHAR(255) NOT NULL,
	"email"	VARCHAR(255),
	"token"	VARCHAR(255),
	"created"	DATE,
	"age"	TEXT,
	"weight"	FLOAT,
	"height"	FLOAT,
	"gender"	BLOB CHECK("gender" IN ('Male', 'Female')),
	"activity"	BLOB CHECK("activity" IN ('No Exercise', 'Once a week', '2-3 time per week', '4-5 times a week')),
	"goal"	BLOB CHECK("goal" IN ('maintain weight', 'lose weight', 'gain weight')),
	"bmi"	FLOAT,
	"requiredCalories"	FLOAT, is_verified BOOLEAN DEFAULT FALSE, verification_token TEXT,
	PRIMARY KEY("id"),
	UNIQUE("email")
);
